

public class Professor {
	private String name;
	private String department;
	
	public Professor(String n) {
		name = n;
		department ="Instructional Computing";
	}
	
	public String getName() {
		return name;
	}
	
	public String getDepartment() {
		return department;
	}
	
	public String toString() {
		return name;
	}
}
